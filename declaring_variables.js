var numberVar = 10;
var stringVar = "Hello, world!";
var booleanVar = true;

console.log("Before reassignment:");
console.log("numberVar:", numberVar);
console.log("stringVar:", stringVar); 
console.log("booleanVar:", booleanVar);

numberVar = 20;
stringVar = "Goodbye, world!";
booleanVar = false;

console.log("\nAfter reassignment:");
console.log("numberVar:", numberVar);
console.log("stringVar:", stringVar); 
console.log("booleanVar:", booleanVar); 

